    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/rtp", "root", "");
            String sql = "SELECT sum(mse1+mse2+ta) as internal,count(ta) as count from marks where sem = '" + tsem.getSelectedItem() + "' and id ='" + student_id + "'";
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                internal = Integer.parseInt(rs.getString("internal"));
                count = Integer.parseInt(rs.getString("count"));
                String sql2 = "SELECT sum(marks) as external from ese where sem = '" + tsem.getSelectedItem() + "' and id ='" + student_id + "'";
                PreparedStatement pst2 = con.prepareStatement(sql2);
                ResultSet rs2 = pst2.executeQuery();
                if (rs2.next()) {
                    external = Integer.parseInt(rs2.getString("external"));
                }
                tper.setText(String.valueOf((internal + external) / count));
                if(tper.equals("")) JOptionPane.showMessageDialog(rootPane, "Results yet to be Declared...");
                if (Integer.parseInt(tper.getText()) > 35) {
                    tresult.setText("Pass");
                } else {
                    tresult.setText("Fail");
                }
                rs2.close();
                pst2.close();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Results yet to be Declared...");
            }
            rs.close();
            pst.close();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Results yet to be Declared...");
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

